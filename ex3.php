//създаваме променливи за факториелите.
$n1 = 5;
$n2= 8; 
$n3= 10;

$fact = 1;

//Създаваме цикъл, койтп ще умножава стоностите докато са
// по-големи от 1.
do {
    $fact = ($fact * $n1) + ($fact * $n2) + ($fact * $n3);
    //декрементираме стоностите;
    $n1-- && $n2-- && $n3--;
    
} while (($n1 > 1) && ($n2 > 1) && ($n3>1));

//Извеждаме стоността от сумата на трите факториели.
echo $fact;
